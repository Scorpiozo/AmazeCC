import { useState, useEffect } from 'react';

/**
 * A custom hook that safely parses and writes to localStorage.
 * Prevents app crashes caused by corrupted JSON strings in localStorage.
 */
export function useSafeLocalStorage<T>(key: string, initialValue: T) {
  // State to store our value
  // Pass initial state function to useState so logic is only executed once
  const [storedValue, setStoredValue] = useState<T>(() => {
    if (typeof window === 'undefined') {
      return initialValue;
    }
    try {
      const item = window.localStorage.getItem(key);
      return item ? JSON.parse(item) : initialValue;
    } catch (error) {
      console.warn(`Error parsing localStorage key "${key}":`, error);
      // Fallback to initial value if parsing fails
      return initialValue;
    }
  });

  // Return a wrapped version of useState's setter function that persists the new value
  const setValue = (value: T | ((val: T) => T)) => {
    try {
      const valueToStore =
        value instanceof Function ? value(storedValue) : value;
      
      setStoredValue(valueToStore);
      
      if (typeof window !== 'undefined') {
        window.localStorage.setItem(key, JSON.stringify(valueToStore));
      }
    } catch (error) {
      console.warn(`Error setting localStorage key "${key}":`, error);
    }
  };

  return [storedValue, setValue] as const;
}
